#!/usr/bin/env node
/**
 * Post-build: inlines `dist/index.html` (single-file Vite build) as
 * `dist/index-html.js` (+ matching `.d.ts`) so adapter packages can
 * import it as a regular ES module:
 *
 *   import { indexHtml } from "@aotter/mantle-admin-ui";
 *
 * Why a separate JS module instead of pointing `package.json#main`
 * at `dist/index.html`: bundlers (esbuild via wrangler, Vite, etc.)
 * don't ship a generic .html loader; an `import "*.html"` either
 * needs a Vite `?raw` suffix (Vite-only) or an esbuild loader
 * plugin. A plain JS module sidesteps both.
 */
import { readFileSync, writeFileSync } from "node:fs";
import { fileURLToPath } from "node:url";
import { dirname, resolve } from "node:path";

const here = dirname(fileURLToPath(import.meta.url));
const dist = resolve(here, "..", "dist");
const htmlPath = resolve(dist, "index.html");
const jsPath = resolve(dist, "index-html.js");
const dtsPath = resolve(dist, "index-html.d.ts");

const html = readFileSync(htmlPath, "utf8");
const escaped = JSON.stringify(html);

const banner =
  `// AUTO-GENERATED by scripts/inline-index-html.mjs — DO NOT EDIT.\n` +
  `// Source: dist/index.html\n`;

writeFileSync(jsPath, `${banner}export const indexHtml = ${escaped};\n`);
writeFileSync(dtsPath, `${banner}export declare const indexHtml: string;\n`);

console.log(
  `[inline-index-html] wrote .js + .d.ts from ${html.length}-byte index.html`,
);
